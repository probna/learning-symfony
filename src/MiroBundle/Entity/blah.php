<?php

namespace MiroBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * blah
 *
 * @ORM\Table(name="blah")
 * @ORM\Entity(repositoryClass="MiroBundle\Repository\blahRepository")
 */
class blah {
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    private $name;

    /**
     * @var int
     *
     * @ORM\Column(name="age", type="integer")
     */
    private $age;

    /**
     * @var int
     *
     * @ORM\Column(name="tomo", type="integer")
     */
    private $tomo;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return blah
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set age
     *
     * @param integer $age
     *
     * @return blah
     */
    public function setAge($age)
    {
        $this->age = $age;

        return $this;
    }

    /**
     * Get age
     *
     * @return int
     */
    public function getAge()
    {
        return $this->age;
    }

    /**
     * @return int
     */
    public function getTomo()
    {
        return $this->tomo;
    }

    /**
     * @param int $tomo
     */
    public function setTomo($tomo)
    {
        $this->tomo = $tomo;
    }

    
}

